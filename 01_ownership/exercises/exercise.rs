struct PortfolioItem {
    token: String,
    amount: f64,
}

fn exercise5() {
    let portfolio = vec![
        PortfolioItem { token: String::from("BTC"), amount: 0.5 },
        PortfolioItem { token: String::from("ETH"), amount: 2.0 },
    ];

    let item = find_token(&portfolio, "BTC");
    match item {
        Some(item) => println!("Найдено: {} {}", item.amount, item.token),
        None => println!("Токен не найден"),
    }
}

// ❌ Добавь правильный lifetime 'a
fn find_token<'a>(portfolio: &'a [PortfolioItem], token: &str) -> Option<&'a PortfolioItem> {
    for item in portfolio {
        if item.token == token {
            return Some(item);
        }
    }
    None
}

