// =====================================================================
// 🟢 УРОВЕНЬ 1: ПРОСТОЙ MOVE (ПЕРЕМЕЩЕНИЕ)
// =====================================================================
// #[derive(Debug)] — позволяет печатать структуру через {:?}
#[derive(Debug)]
struct Transaction {
    id: u32,
    from: String,
    to: String,
    amount: f64,
}

// Функция принимает ВЛАДЕНИЕ transaction (move)
// После вызова этой функции оригинал "умрёт"
fn process_transaction(tx: Transaction) {
    println!("📦 Обрабатываю транзакцию #{}: {} -> {} ({} USDT)",
        tx.id, tx.from, tx.to, tx.amount);
} // tx выходит из области видимости — память освобождается

fn level1_move() {
    println!("\n=== 🟢 УРОВЕНЬ 1: MOVE ===");

    // Создаём транзакцию
    let tx1 = Transaction {
        id: 1,
        from: String::from("0xAlice"),
        to: String::from("0xBob"),
        amount: 100.0,
    };

    // Передаём ВЛАДЕНИЕ в функцию
    process_transaction(tx1);
    // ⚠️ На этот момент tx1 БОЛЬШЕ НЕ СУЩЕСТВУЕТ!

    // ❌ Раскомментируй и увидишь ошибку!
    // println!("tx1: {:?}", tx1);
    // ^ ОШИБКА: borrow of moved value: `tx1`
}

// =====================================================================
// 🟡 УРОВЕНЬ 2: ВОЗВРАТ ВЛАДЕНИЯ
// =====================================================================

// Функция забирает владение, но ВОЗВРАЩАЕТ его вместе с результатом
fn process_and_return(mut tx: Transaction) -> (Transaction, bool) {
    if tx.amount > 0.0 {
        tx.amount *= 0.999; // комиссия 0.1%
        println!("✅ Транзакция #{} обработана. Комиссия списана.", tx.id);
        (tx, true)
    } else {
        println!("❌ Транзакция #{} отклонена: сумма = 0", tx.id);
        (tx, false)
    }
}

fn level2_move_return() {
    println!("\n=== 🟡 УРОВЕНЬ 2: ВОЗВРАТ ВЛАДЕНИЯ ===");

    let tx = Transaction {
        id: 2,
        from: String::from("0xBob"),
        to: String::from("0xCharlie"),
        amount: 50.0,
    };

    // Передаём владение и получаем его обратно
    let (tx, success) = process_and_return(tx);

    if success {
        println!("✅ Успех! Теперь tx снова моя: {}", tx.id);
    }

    // ✅ Теперь tx снова жив!
    println!("tx всё ещё доступна: #{}", tx.id);
}

// =====================================================================
// 🟠 УРОВЕНЬ 3: БЕЗОПАСНОСТЬ ЧЕРЕЗ ССЫЛКИ (&T)
// =====================================================================

// Функция принимает ССЫЛКУ — не забирает владение
fn print_transaction(tx: &Transaction) {
    println!("📋 Транзакция #{}: {} -> {} ({} USDT)",
        tx.id, tx.from, tx.to, tx.amount);
} // tx — это ссылка, память НЕ освобождается

fn level3_reference() {
    println!("\n=== 🟠 УРОВЕНЬ 3: ССЫЛКИ (&T) ===");

    let tx = Transaction {
        id: 3,
        from: String::from("0xCharlie"),
        to: String::from("0xDave"),
        amount: 200.0,
    };

    // Передаём ССЫЛКУ на tx, а не сам tx
    print_transaction(&tx);

    // ✅ tx всё ещё жив!
    println!("tx всё ещё здесь: #{}", tx.id);
    println!("И я могу передать её снова!");
    print_transaction(&tx);
}

// =====================================================================
// 🔴 УРОВЕНЬ 4: ИЗМЕНЯЕМЫЕ ССЫЛКИ (&mut T)
// =====================================================================

fn apply_commission(tx: &mut Transaction) {
    // Изменяем транзакцию через ссылку
    tx.amount *= 0.999; // комиссия 0.1%
    tx.id = tx.id + 100; // меняем ID для отметки
    println!("💰 Комиссия применена. Новая сумма: {} USDT", tx.amount);
}

fn level4_mut_reference() {
    println!("\n=== 🔴 УРОВЕНЬ 4: ИЗМЕНЯЕМЫЕ ССЫЛКИ (&mut T) ===");

    // ⚠️ Чтобы менять через ссылку, переменная должна быть mut
    let mut tx = Transaction {
        id: 4,
        from: String::from("0xDave"),
        to: String::from("0xEve"),
        amount: 300.0,
    };

    println!("До комиссии: {} USDT", tx.amount);

    // Передаём изменяемую ссылку
    apply_commission(&mut tx);

    println!("После комиссии: {} USDT", tx.amount);
    println!("Новый ID: {}", tx.id);
}

