// ============================================
// 🔵 Шаг 1: Запись и чтение текстовых файлов
// ============================================
//
// КОНТЕКСТ: Сохраняем и загружаем мнемоническую фразу кошелька.
//
// ЧТО ТЫ УЗНАЕШЬ:
// - fs::write() — запись строки в файл
// - fs::read_to_string() — чтение всего файла в строку
// - fs::remove_file() — удаление файла
// - fs::metadata() — проверка существования файла
// - ? оператор для обработки ошибок

use std::fs;

// Главная функция возвращает Result — так мы можем использовать ?
// std::io::Result<()> — это Result<(), std::io::Error>
fn main() -> std::io::Result<()> {
    // ===========================================
    // 1. ЗАПИСЬ В ФАЙЛ
    // ===========================================
    //
    // fs::write(путь, содержимое) — создаёт или перезаписывает файл
    //
    // 💡 ADVICE: fs::write() — самый простой способ записать данные.
    // Он создаёт файл, пишет и закрывает его за одну операцию.
    // Идеально для небольших файлов (< 100 МБ).
    //
    // ПОЧЕМУ: Мы используем здесь ? вместо .expect()?
    // Потому что main() возвращает Result, и ошибка будет
    // автоматически распечатана при выходе из программы.
    // Это стандартный способ для серьёзного кода.
    fs::write("mnemonic.txt", "abandon able about above absent absorb abstract")?;
    println!("✅ Мнемоническая фраза сохранена в mnemonic.txt");

    // ===========================================
    // 2. ЧТЕНИЕ ИЗ ФАЙЛА
    // ===========================================
    //
    // fs::read_to_string(путь) — читает ВЕСЬ файл в String
    //
    // 💡 ADVICE: Этот метод читает файл целиком в память.
    // Для конфигов, ключей, небольших JSON — идеально.
    // Для файлов > 100 МБ — используй BufReader (см. пример 04).
    //
    // ПОЧЕМУ: Если файл больше доступной памяти — программа упадёт.
    // Всегда знай размер файла перед чтением целиком.
    let phrase = fs::read_to_string("mnemonic.txt")?;
    println!("📖 Прочитано из файла: {}", phrase.trim());

    // ===========================================
    // 3. ПРОВЕРКА СУЩЕСТВОВАНИЯ ФАЙЛА
    // ===========================================
    //
    // fs::metadata(путь) — возвращает информацию о файле.
    // Если файла нет — возвращает ошибку NotFound.
    // Мы проверяем .is_ok() — значит файл существует.
    //
    // 💡 ADVICE: Всегда проверяй, что файл существует, прежде чем его читать.
    // Это спасёт тебя от неожиданных падений программы.
    if fs::metadata("mnemonic.txt").is_ok() {
        println!("📁 Файл mnemonic.txt существует");
    }

    // ===========================================
    // 4. ДОБАВЛЕНИЕ ТЕКСТА В КОНЕЦ ФАЙЛА
    // ===========================================
    //
    // OpenOptions — позволяет настроить режим открытия файла.
    // .append(true) — открыть для добавления в конец.
    // .create(true) — создать файл, если его нет.
    //
    // ПОЧЕМУ: Если мы используем fs::write() — он перезапишет файл.
    // А нам нужно ДОБАВИТЬ строку, не стирая старые данные.
    // Для этого нужен OpenOptions с append.
    use std::io::Write;  // writeln! — макрос из std::io::Write

    let mut file = std::fs::OpenOptions::new()
        .append(true)   // добавляем в конец
        .create(true)   // создаём, если нет
        .open("mnemonic.txt")?;

    // writeln! — как println!, но пишет в файл, а не в консоль
    writeln!(file, "cactus calm canal caption cargo cash casino")?;
    println!("➕ Добавлены новые слова в конец файла");

    // Читаем и показываем результат
    let updated = fs::read_to_string("mnemonic.txt")?;
    println!("📋 Теперь в файле:");
    for (i, word) in updated.lines().enumerate() {
        println!("  {}: {}", i + 1, word);
    }

    // ===========================================
    // 5. УДАЛЕНИЕ ФАЙЛА
    // ===========================================
    //
    // fs::remove_file(путь) — удаляет файл.
    // Если файла нет — вернёт ошибку.
    //
    // 💡 ADVICE: Удаляй временные файлы в конце программы,
    // чтобы не мусорить на диске пользователя.
    //
    // ПОЧЕМУ: Мы удаляем в конце, чтобы при повторном запуске
    // не было ошибки "файл уже существует" и чтобы не оставлять мусор.
    fs::remove_file("mnemonic.txt")?;
    println!("🗑️ Временный файл удалён");

    println!();
    println!("🎉 Урок 1 завершён! Ты научился:");
    println!("   - Записывать текст в файл");
    println!("   - Читать текст из файла");
    println!("   - Добавлять текст в конец");
    println!("   - Проверять существование файла");
    println!("   - Удалять файл");

    Ok(())
}
