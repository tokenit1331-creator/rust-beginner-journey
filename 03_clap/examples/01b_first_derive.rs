// Это МИНИМУМ, который нужно знать.
// Всего 1 аргумент. Без флагов. Без дефолтов. Без Option.
//
// ============================================================

use clap::Parser;

#[derive(Parser)]
struct Args {
    /// Имя пользователя (единственный аргумент)
    name: String,
}

fn main() {
    let args = Args::parse();
    println!("Привет, {}!", args.name);
}

