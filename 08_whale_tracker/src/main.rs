// хочу парсить аргументы командной строки и иметь подкоманды
// почему clap? — потому что писать парсер argv вручную это боль, clap делает это за тебя
// зачем два трейта? — Parser нужен для самой структуры Cli, Subcommand — для enum с командами, без обоих не скомпилируется
use clap::{Parser, Subcommand};

// хочу читать и писать файлы
// почему std::fs? — это стандартная библиотека, ничего лишнего тащить не надо
// зачем вообще файл? — список китов должен где-то храниться между запусками программы, иначе всё теряется
use std::fs;

// хочу проверить существует ли файл перед чтением
// почему Path, а не просто строка? — Path даёт метод .exists(), со строкой так не сделаешь
// зачем проверять? — если файла нет и мы попробуем его прочитать, получим ошибку, лучше обработать заранее
use std::path::Path;

// хочу описать что такое один кит
// почему struct? — кит это набор связанных полей, не просто одно значение
// зачем Debug? — чтобы можно было напечатать кита через {:?} при отладке, без этого компилятор не даст
// зачем Clone? — чтобы можно было скопировать запись когда нужно, без этого Rust не позволит
#[derive(Debug, Clone)]
struct Whale {
    // почему String, а не &str? — String владеет данными, &str это просто ссылка на чужие данные
    // зачем владение? — Whale живёт сам по себе, не зависит от того откуда пришли данные
    address: String,
    label: String,
    // почему threshold тоже String, а не f64? — не делаем арифметику, только храним и показываем,
    // зачем такое решение? — меньше конвертаций, пустая строка это тоже валидное состояние (порог не задан)
    threshold: String,
}

// хочу добавить методы к структуре Whale
// почему impl блок? — в Rust методы живут отдельно от полей, это не класс как в Java
// зачем вообще методы? — чтобы логика парсинга и сериализации жила рядом с данными, а не размазана по всему файлу
impl Whale {
    // хочу создать Whale из одной строки файла
    // почему статический метод без &self? — у нас ещё нет экземпляра, мы его только создаём
    // зачем Option<Self>? — строка может оказаться пустой или битой, None это способ сказать "не получилось" без паники
    fn from_line(line: &str) -> Option<Self> {
        // ШАГ 1: сначала разбиваем строку на части
        // почему именно первым? — нельзя проверить количество частей или обратиться к parts[0]
        // пока у нас нет самого вектора, сначала нужно его получить
        // почему '|'? — запятая могла бы быть в метке, вертикальная черта встречается реже
        // зачем collect()? — split даёт итератор, а мне нужен Vec чтобы обращаться по индексу [0], [1], [2]
        let parts: Vec<&str> = line.split('|').collect();

        // ШАГ 2: только теперь проверяем длину — потому что parts уже существует
        // почему проверка идёт до обращения к элементам? — если бы мы написали parts[0] до этой проверки,
        // программа упала бы с паникой на пустой строке, сначала убеждаемся что данные есть
        // почему >= 2? — адрес и метка обязательны, порог нет, значит минимум две части
        if parts.len() >= 2 {
            // ШАГ 3: только после успешной проверки строим Whale и оборачиваем в Some
            // почему Some идёт последним? — нельзя обернуть в Some то чего ещё нет,
            // сначала убедились что данные валидны (шаг 2), теперь безопасно читаем parts[0] и parts[1]
            // почему Some(...)? — это способ Rust сказать "вот результат, он есть"
            // зачем оборачивать? — функция возвращает Option, голый Whale не подойдёт по типу
            Some(Whale {
                // почему to_string()? — parts[0] это &str, ссылка на кусок строки,
                // зачем конвертировать? — Whale должен владеть своими данными, а не ссылаться на временный буфер
                address: parts[0].to_string(),
                label: parts[1].to_string(),
                // почему тернарный if? — порог может отсутствовать, это нормально
                // зачем String::new()? — создаёт пустую строку без аллокации, дёшево
                threshold: if parts.len() > 2 { parts[2].to_string() } else { String::new() },
            })
        } else {
            // ШАГ 3 (альтернативный): проверка не прошла — возвращаем None
            // почему None идёт в else, а не раньше? — мы не знали что строка битая пока не проверили длину
            // почему None? — это идиоматичный способ в Rust сказать "ничего не вышло"
            // зачем не паниковать? — битая строка в файле не должна убивать всю программу
            None
        }
    }

    // хочу превратить кита обратно в строку для записи в файл
    // почему &self? — только читаю поля, не меняю, изменяемость не нужна
    // зачем отдельный метод? — чтобы формат хранения был в одном месте, а не копипастился везде
    fn to_line(&self) -> String {
        // почему format!? — самый простой способ собрать строку из нескольких частей
        // зачем \n в конце? — каждый кит должен быть на своей строке, иначе при чтении всё сольётся в одну
        format!("{}|{}|{}\n", self.address, self.label, self.threshold)
    }
}

// хочу описать все команды которые умеет программа
// почему enum? — команды взаимоисключающие, в один момент времени выполняется ровно одна
// зачем derive(Subcommand)? — clap смотрит на этот атрибут и генерирует весь парсинг подкоманд автоматически
#[derive(Subcommand)]
enum Commands {
    // почему /// а не //? — тройной слеш это doc-комментарий, clap вытаскивает его в текст --help
    // зачем это? — пользователь увидит описание команды когда напишет whale --help
    /// Добавить кошелёк для отслеживания
    Watch {
        // почему String, а не &str? — clap отдаёт нам владение аргументами, ссылку тут не сделать
        // зачем позиционный аргумент? — пользователь пишет whale watch 0xABC "Binance", без флагов, удобнее
        address: String,
        label: String,
        // почему Option<String>? — порог необязателен, пользователь может его не указывать
        // зачем Option, а не просто пустая строка по умолчанию? — clap сам поймёт что аргумент необязателен
        threshold: Option<String>,
    },

    /// Показать все отслеживаемые кошельки
    // почему вариант без полей? — команде list не нужны аргументы, просто показать всё
    // зачем такой вариант вообще? — это самая частая операция, должна быть простой
    List,

    /// Удалить кошелёк по номеру из списка
    Remove {
        // почему usize? — номер не может быть отрицательным, usize это беззнаковое целое
        // зачем 1-based? — пользователи считают с 1, не с 0, меньше путаницы
        number: usize,
    },

    /// Поиск по адресу или метке
    Search {
        // почему String? — запрос может быть любой длины, clap отдаёт владение
        // зачем поиск? — список может вырасти, листать глазами неудобно
        query: String,
    },
}

// хочу описать корневую структуру всего CLI
// почему derive(Parser)? — clap генерирует метод Cli::parse() который читает argv и заполняет структуру
// зачем отдельная структура? — это точка входа для clap, всё остальное вешается на неё
#[derive(Parser)]
// почему этот атрибут? — задаёт имя программы, версию и описание для --help
// зачем version и about? — без них --help выглядит голо, пользователь не понимает что за программа
#[command(name = "whale", version = "1.0", about = "Отслеживание китов в крипте")]
struct Cli {
    // почему #[command(subcommand)]? — говорит clap что это поле не флаг и не аргумент, а подкоманда
    // зачем это поле? — здесь будет лежать распознанная команда после парсинга argv
    #[command(subcommand)]
    command: Commands,
}

// хочу не писать путь к файлу в каждой функции руками
// почему константа? — если захочу переименовать файл, меняю в одном месте, а не ищу по всему коду
// зачем &str со static lifetime? — строковый литерал живёт всю программу, это самый дешёвый вариант
const DATA_FILE: &str = "whales.txt";

// хочу загрузить всех китов из файла в память
// почему Vec<Whale>? — количество записей заранее неизвестно, вектор растёт динамически
// зачем загружать всё сразу? — программа короткоживущая, проще держать всё в памяти чем делать запросы к файлу
fn load_whales() -> Vec<Whale> {
    // ШАГ 1: сначала проверяем существование файла
    // почему именно первым? — если файла нет, все следующие шаги бессмысленны и упадут с ошибкой,
    // дешевле проверить один раз и выйти, чем пытаться читать несуществующий файл
    // почему проверяю exists()? — при первом запуске файла нет, это нормально, не ошибка
    // зачем ранний return? — дальше нечего делать, возвращаю пустой список
    if !Path::new(DATA_FILE).exists() {
        return Vec::new();
    }

    // ШАГ 2: только теперь читаем файл — мы уже знаем что он существует
    // почему чтение идёт после проверки, а не до? — read_to_string на несуществующем файле вернёт Err,
    // мы бы обработали его через unwrap_or_else, но явная проверка exists() понятнее по намерению
    // почему unwrap_or_else? — если файл есть но прочитать не удалось (права доступа и т.д.), не паникую
    // зачем пустая строка как fallback? — тогда lines() вернёт пустой итератор и я получу пустой список
    let content = fs::read_to_string(DATA_FILE).unwrap_or_else(|_| String::new());

    // ШАГ 3: создаём пустой контейнер для результатов — нужен до цикла, потому что цикл в него пишет
    // почему mut? — буду добавлять элементы через push, без mut компилятор не даст
    // зачем Vec::new()? — начинаю с пустого, заполняю в цикле
    let mut whales = Vec::new();

    // ШАГ 4: идём по строкам и заполняем вектор — только теперь, когда content и whales уже есть
    // почему цикл идёт после создания whales? — нельзя писать в вектор который ещё не объявлен
    // почему lines()? — разбивает по \n и убирает символы переноса, не надо делать это вручную
    for line in content.lines() {
        // почему if let Some(...)? — это паттерн-матчинг, если from_line вернул None — строка просто пропускается
        // зачем такой подход? — файл мог быть повреждён, одна плохая строка не должна ломать всё
        if let Some(whale) = Whale::from_line(line) {
            whales.push(whale);
        }
    }

    // почему без return и без ;? — в Rust последнее выражение в функции это возвращаемое значение
    // зачем такой стиль? — это идиоматичный Rust, return пишут только для раннего выхода
    whales
}

// хочу сохранить всех китов обратно в файл
// почему &[Whale]? — срез, не требует владения вектором, функция только читает данные
// зачем полная перезапись? — проще чем дописывать или патчить, файл маленький
fn save_whales(whales: &[Whale]) {
    // ШАГ 1: сначала собираем всё содержимое в строку в памяти
    // почему не пишем сразу в файл в цикле? — если писать построчно и на середине упадёт ошибка,
    // файл окажется наполовину записан и испорчен, лучше сначала собрать всё, потом одним вызовом записать
    // почему String::new()? — начинаю пустым, буду наращивать
    let mut content = String::new();

    // ШАГ 2: заполняем строку — только после того как content объявлен
    for whale in whales {
        // почему push_str? — добавляет &str в конец String без новой аллокации
        // зачем & перед to_line()? — to_line() возвращает String, & превращает его в &str который принимает push_str
        content.push_str(&whale.to_line());
    }

    // ШАГ 3: только теперь пишем в файл — когда content полностью готов
    // почему запись идёт после цикла, а не внутри? — атомарность: либо записываем всё, либо ничего
    // почему if let Err(e)? — запись может упасть (диск полон, нет прав), игнорировать это нельзя
    // зачем eprintln! а не println!? — ошибки принято писать в stderr, а не в stdout, это unix-конвенция
    if let Err(e) = fs::write(DATA_FILE, content) {
        eprintln!("Ошибка сохранения: {}", e);
    }
}

// хочу добавить кошелёк в список отслеживания
// почему &mut Vec? — буду вызывать push, без mut компилятор запретит изменение
// зачем String а не &str для аргументов? — clap отдаёт нам владение, ссылку тут не сделать
fn watch_whale(whales: &mut Vec<Whale>, address: String, label: String, threshold: Option<String>) {
    // ШАГ 1: нормализуем threshold из Option<String> в String
    // почему именно первым? — Whale ожидает String, не Option,
    // нужно привести тип до создания структуры, иначе компилятор не пропустит
    // почему unwrap_or_default()? — если порог был передан берём его, если нет — пустая строка
    // зачем shadowing? — переопределяю переменную с тем же именем, теперь threshold это String, не Option
    let threshold = threshold.unwrap_or_default();

    // ШАГ 2: создаём запись — только после того как все поля имеют правильный тип
    // почему создание идёт до push? — нельзя добавить в вектор то чего ещё нет
    // почему shorthand синтаксис { address, label, threshold }? — когда имя поля совпадает с именем переменной,
    // зачем писать address: address? — можно просто address, Rust поймёт, меньше шума
    let new_whale = Whale { address, label, threshold };

    // ШАГ 3: добавляем в вектор, потом сохраняем на диск, потом сообщаем пользователю
    // почему именно такой порядок? — сначала меняем состояние в памяти (push),
    // потом синхронизируем с диском (save), потом подтверждаем что всё прошло (println),
    // если поменять порядок и сначала вывести сообщение — пользователь увидит "добавлен" даже если save упал
    whales.push(new_whale);
    save_whales(whales);
    println!("✅ Кошелёк добавлен (всего: {})", whales.len());
}

// хочу вывести всех китов на экран
// почему &[Whale]? — только читаю, изменяемость не нужна, срез дешевле вектора
// зачем отдельная функция? — логика вывода может понадобиться в нескольких местах (list и search)
fn list_whales(whales: &[Whale]) {
    // хочу обработать пустой список отдельно
    // почему is_empty()? — читается как человеческий язык, лучше чем len() == 0
    // зачем ранний return? — дальше нечего делать, цикл по пустому срезу просто ничего не выведет, но явный return понятнее
    if whales.is_empty() {
        println!("Нет отслеживаемых кошельков.");
        return;
    }

    // хочу нумерацию с 1
    // почему enumerate()? — добавляет индекс к каждому элементу итератора, не надо заводить счётчик вручную
    // зачем iter()? — даёт ссылки &Whale, не забирает владение, записи остаются в векторе
    for (i, w) in whales.iter().enumerate() {
        // хочу показать что-то вместо пустой строки для порога
        // почему "-"? — пустая строка в выводе выглядит как баг, прочерк явно говорит "не задан"
        // зачем &w.threshold? — threshold это String, & даёт &str который совместим с "-" в условии
        let threshold_display = if w.threshold.is_empty() { "-" } else { &w.threshold };

        // почему i + 1? — индексы с нуля для программиста, нумерация с единицы для пользователя
        println!("{}. {} | {} | порог: {}", i + 1, w.label, w.address, threshold_display);
    }
}

// хочу удалить кита по номеру
// почему &mut Vec? — remove() меняет вектор, без mut нельзя
// зачем number: usize? — номер не может быть отрицательным, usize это гарантирует на уровне типа
fn remove_whale(whales: &mut Vec<Whale>, number: usize) {
    // ШАГ 1: сначала валидируем номер
    // почему именно первым? — если сразу вызвать whales.remove(number - 1) с невалидным number,
    // получим панику (выход за границы) или переполнение usize при number == 0,
    // проверка должна идти до любого обращения к вектору по индексу
    // почему number == 0? — usize не может быть отрицательным, но ноль тоже невалиден, проверяю явно
    // зачем number > whales.len()? — защита от "удали кита 999 из списка из 3 штук"
    if number == 0 || number > whales.len() {
        eprintln!("❌ Неверный номер.");
        return;
    }

    // ШАГ 2: удаляем элемент — только после того как убедились что индекс валиден
    // почему remove идёт до save? — нельзя сохранить список без удалённого элемента пока он ещё там есть
    // почему remove() возвращает элемент? — Rust отдаёт владение удалённым элементом, я могу его использовать
    // зачем number - 1? — пользователь считает с 1, вектор с 0, надо перевести
    let removed = whales.remove(number - 1);

    // ШАГ 3: сохраняем и сообщаем — в том же порядке что в watch_whale
    // почему save идёт до println? — сначала фиксируем изменение на диске, потом говорим что всё ок,
    // если наоборот — пользователь увидит "удалён" даже если запись на диск упала
    save_whales(whales);
    println!("✅ Кошелёк '{}' удалён.", removed.label);
}

// хочу найти китов по подстроке
// почему &[Whale] и &str? — только читаю, владение не нужно, ссылки дешевле
// зачем отдельная функция? — поиск это самостоятельная операция, не надо мешать с выводом
fn search_whales(whales: &[Whale], query: &str) {
    // ШАГ 1: нормализуем запрос один раз до цикла
    // почему именно первым? — если делать to_lowercase() внутри цикла на каждой итерации,
    // мы создаём новую String на каждый кит, делаем это один раз снаружи — платим один раз
    // почему to_lowercase()? — "binance" и "Binance" должны находить одно и то же
    let query_lower = query.to_lowercase();

    // ШАГ 2: создаём контейнер для результатов до цикла — цикл в него пишет
    // почему Vec ссылок, а не Vec<Whale>? — не хочу клонировать записи, просто храню указатели на них
    // зачем вообще собирать? — чтобы проверить is_empty() и вывести "ничего не найдено" если пусто
    let mut results = Vec::new();

    // ШАГ 3: ищем — только теперь, когда query_lower и results уже готовы
    // почему цикл идёт после объявления results? — нельзя писать в вектор который ещё не объявлен
    for w in whales {
        // почему contains()? — ищет вхождение подстроки, не точное совпадение
        // зачем искать и по метке и по адресу? — пользователь может не помнить что именно вводил
        if w.label.to_lowercase().contains(&query_lower) || w.address.to_lowercase().contains(&query_lower) {
            results.push(w);
        }
    }

    if results.is_empty() {
        println!("Ничего не найдено.");
    } else {
        // почему тот же формат что в list_whales? — пользователь видит одинаковый вывод, не путается
        // зачем results.iter()? — results это Vec<&Whale>, iter() даёт &&Whale, но Rust разыменует автоматически
        for (i, w) in results.iter().enumerate() {
            let threshold_display = if w.threshold.is_empty() { "-" } else { &w.threshold };
            println!("{}. {} | {} | порог: {}", i + 1, w.label, w.address, threshold_display);
        }
    }
}

// точка входа в программу
// почему fn main()? — это соглашение, ОС знает что искать функцию с таким именем
// зачем она вообще нужна? — без неё компилятор не знает с чего начинать выполнение
fn main() {
    // ШАГ 1: парсим аргументы — самое первое, потому что без этого не знаем что делать
    // почему именно первым? — cli.command нужен для match ниже, без него match невозможен
    // почему Cli::parse()? — clap читает std::env::args() и заполняет структуру Cli
    // зачем доверять это clap? — он сам выводит ошибки и --help, мне не надо писать это руками
    let cli = Cli::parse();

    // ШАГ 2: загружаем данные — после парсинга аргументов, но до выполнения команды
    // почему именно здесь? — все четыре команды работают с whales, загружаем один раз для всех
    // если загружать внутри каждой ветки match — дублирование кода и лишние чтения файла
    // почему mut? — команды watch и remove будут менять вектор
    let mut whales = load_whales();

    // ШАГ 3: выполняем команду — только когда и аргументы и данные уже готовы
    // почему match идёт последним? — нельзя выполнить команду не зная какая она (cli) и не имея данных (whales)
    // почему match? — исчерпывающий, компилятор заставит обработать все варианты enum
    // зачем деструктурировать поля прямо в паттерне? — сразу получаю переменные, не надо писать cmd.address и т.д.
    match cli.command {
        // почему &mut whales для Watch и Remove? — они меняют вектор
        // зачем & для List и Search? — они только читают, изменяемость не нужна, компилятор это проверит
        Commands::Watch { address, label, threshold } => watch_whale(&mut whales, address, label, threshold),
        Commands::List                                => list_whales(&whales),
        Commands::Remove { number }                   => remove_whale(&mut whales, number),
        // почему &query? — search_whales принимает &str, query это String, & даёт нужный тип
        Commands::Search { query }                    => search_whales(&whales, &query),
    }
    // почему не надо явно освобождать память? — Rust делает это сам через Drop когда переменные выходят из области видимости
    // зачем это знать? — чтобы не писать free() как в C и не бояться утечек
}
