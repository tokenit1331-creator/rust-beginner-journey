// ============================================
// 🔴 Шаг 4: CSV + BufReader (табличные данные)
// ============================================
//
// КОНТЕКСТ: Разбираем историю транзакций из CSV-файла.
//
// ЧТО ТЫ УЗНАЕШЬ:
// - BufReader — чтение файла без загрузки всего в память
// - .split() — разделение строк CSV
// - Парсинг полей: дата, число, перечисление
// - Обработка ошибок в каждой строке
// - Фильтрация и анализ данных из файла

use std::fs::File;
use std::io::{BufRead, BufReader};

fn main() -> std::io::Result<()> {
    // ===========================================
    // 1. СОЗДАЁМ CSV-ФАЙЛ С ТРАНЗАКЦИЯМИ
    // ===========================================
    //
    // В реальном проекте ты получишь этот файл от биржи.
    // Здесь мы создаём его для примера.
    //
    // Формат CSV (Comma-Separated Values):
    //   дата,тип,сумма,токен,статус
    //   каждая строка — одна транзакция
    //
    // 💡 ADVICE: CSV — один из самых популярных форматов
    // для обмена табличными данными. Все биржи,
    // банки и API умеют экспортировать в CSV.
    let csv_content = "\
date,type,amount,token,status
2024-01-15,send,0.5,ETH,completed
2024-01-16,receive,1.0,ETH,completed
2024-01-17,send,200.0,USDT,pending
2024-01-18,receive,50.0,SOL,completed
2024-01-19,send,0.1,ETH,failed
2024-01-20,receive,1000.0,USDT,completed
2024-01-21,send,5.0,SOL,pending
2024-01-22,receive,0.25,ETH,completed
2024-01-23,send,300.0,USDT,completed
2024-01-24,receive,10.0,SOL,failed";

    // Записываем CSV-файл
    std::fs::write("transactions.csv", csv_content)?;
    println!("✅ Создан файл transactions.csv с 10 транзакциями");

    // ===========================================
    // 2. ЧТЕНИЕ CSV ЧЕРЕЗ BufReader
    // ===========================================
    //
    // File::open() — открывает файл.
    // BufReader::new() — оборачивает файл в буферизированный читатель.
    //
    // 💡 ADVICE: BufReader читает данные большими кусками
    // (8 КБ по умолчанию) и отдаёт тебе по одной строке.
    // Это намного быстрее, чем читать по одному байту,
    // и намного экономичнее, чем читать весь файл целиком.
    //
    // ПОЧЕМУ: Если файл размером 10 ГБ — fs::read_to_string()
    // попытается загрузить 10 ГБ в память и упадёт с Out Of Memory.
    // BufReader читает по кускам — память не страдает.
    let file = File::open("transactions.csv")?;
    let reader = BufReader::new(file);

    // .lines() — возвращает итератор по строкам файла.
    // Каждая строка — Result<String, io::Error>.
    //
    // ПОЧЕМУ: Мы используем .flatten() вместо ? в цикле?
    // Потому что ? внутри итератора — это сложно.
    // .flatten() убирает все Err-строки (если они есть),
    // оставляя только Ok(String).
    //
    // Внимание: для настоящего продакшена лучше обрабатывать
    // каждую ошибку отдельно, а не проглатывать.
    let lines: Vec<String> = reader
        .lines()           // итератор Result<String, io::Error>
        .flatten()         // убираем ошибки, оставляем только строки
        .collect();        // собираем в Vec<String>

    // ===========================================
    // 3. ПАРСИНГ CSV-СТРОК
    // ===========================================
    //
    // Первая строка — заголовок.
    // Мы её пропускаем (.skip(1)).
    //
    // Каждая последующая строка — транзакция.
    // Формат: дата,тип,сумма,токен,статус

    // Определяем структуру для транзакции
    // (существует только внутри этой функции)
    struct Transaction {
        date: String,
        tx_type: String,
        amount: f64,
        token: String,
        status: String,
    }

    // Парсим строку в структуру
    // split(',') — разбивает строку на части по запятой
    // collect::<Vec<&str>>() — собирает части в вектор
    fn parse_transaction(line: &str) -> Option<Transaction> {
        let parts: Vec<&str> = line.split(',').collect();

        // Должно быть ровно 5 полей
        // Если строк меньше — это невалидная строка, пропускаем
        //
        // 💡 ADVICE: Всегда проверяй количество полей.
        // CSV может содержать пустые строки или мусор.
        if parts.len() != 5 {
            return None;
        }

        // Парсим сумму в число f64
        // parse() — стандартный метод для преобразования строки в число
        // Если не получилось — возвращаем None (пропускаем строку)
        let amount: f64 = parts[2].parse().ok()?;

        Some(Transaction {
            date: parts[0].to_string(),
            tx_type: parts[1].to_string(),
            amount,
            token: parts[3].to_string(),
            status: parts[4].to_string(),
        })
    }

    // Парсим все строки (кроме заголовка)
    let transactions: Vec<Transaction> = lines
        .iter()              // итерируем по строкам
        .skip(1)             // пропускаем заголовок
        .filter_map(|line| parse_transaction(line))  // парсим, убираем None
        .collect();          // собираем в вектор

    println!("📖 Прочитано {} транзакций", transactions.len());

    // ===========================================
    // 4. АНАЛИЗ ДАННЫХ
    // ===========================================
    //
    // Считаем статистику:
    // - Сколько всего отправлено и получено
    // - Какие транзакции завершились успешно
    // - Какие токены используются

    let mut total_sent = 0.0_f64;
    let mut total_received = 0.0_f64;
    let mut completed = 0;
    let mut failed = 0;

    for tx in &transactions {
        if tx.tx_type == "send" {
            total_sent += tx.amount;
        } else {
            total_received += tx.amount;
        }

        match tx.status.as_str() {
            "completed" => completed += 1,
            "failed" => failed += 1,
            _ => {}
        }
    }

    println!();
    println!("📊 СТАТИСТИКА:");
    println!("   Отправлено:  {:.2} токенов", total_sent);
    println!("   Получено:    {:.2} токенов", total_received);
    println!("   Успешно:     {}", completed);
    println!("   Ошибок:      {}", failed);
    println!("   Чистый доход: {:.2}", total_received - total_sent);

    // ===========================================
    // 5. ТОП ТРАНЗАКЦИЙ
    // ===========================================
    //
    // Находим самую большую транзакцию
    //
    // 💡 ADVICE: max_by — мощный метод итераторов.
    // Он находит элемент с максимальным значением
    // по заданному критерию.
    let max_tx = transactions.iter()
        .max_by(|a, b| a.amount.partial_cmp(&b.amount).unwrap());

    if let Some(biggest) = max_tx {
        println!();
        println!("🏆 Самая большая транзакция:");
        println!("   {} {} {} от {}",
            biggest.amount, biggest.token, biggest.tx_type, biggest.date);
    }

    // ===========================================
    // 6. УБОРКА
    // ===========================================
    std::fs::remove_file("transactions.csv")?;
    println!();
    println!("🗑️ Временный файл удалён");

    println!();
    println!("🎉 Урок 4 завершён! Ты научился:");
    println!("   - Читать файл через BufReader (экономия памяти)");
    println!("   - Парсить CSV-строки");
    println!("   - Анализировать табличные данные");
    println!("   - Обрабатывать ошибки в отдельных строках");

    Ok(())
}
