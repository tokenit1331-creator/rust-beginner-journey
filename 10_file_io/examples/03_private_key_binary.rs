// ============================================
// 🟠 Шаг 3: Бинарные файлы (приватные ключи)
// ============================================
//
// КОНТЕКСТ: Работа с приватными ключами и логами транзакций.
//
// ЧТО ТЫ УЗНАЕШЬ:
// - fs::read() — чтение байтов из файла
// - write_all() — запись байтов в файл
// - OpenOptions для добавления (append) и создания
// - Чем бинарный файл отличается от текстового
// - Как читать и писать массивы байтов

use std::fs;
use std::io::Write;

fn main() -> std::io::Result<()> {
    // ===========================================
    // 1. ЧТО ТАКОЕ БИНАРНЫЙ ФАЙЛ?
    // ===========================================
    //
    // Текстовый файл: "0x1a2b3c" — 8 символов = 8 байт
    // Бинарный файл:  [0x1a, 0x2b, 0x3c] — 3 байта
    //
    // 💡 ADVICE: Приватный ключ — это 32 байта (256 бит).
    // В текстовом виде (hex) он занимает 64 символа.
    // В бинарном — ровно 32 байта. Экономим место и время.
    //
    // ПОЧЕМУ: Бинарный формат — это "родной" формат данных.
    // Не нужно тратить время на преобразование hex → байты и обратно.
    // Кроме того, бинарный файл нельзя случайно прочитать глазами
    // и запомнить приватный ключ (безопасность).

    // Симулируем приватный ключ (32 байта)
    // В реальности ты получишь его от генератора кошелька
    let private_key: [u8; 32] = [
        0x12, 0x34, 0x56, 0x78, 0x9a, 0xbc, 0xde, 0xf0,
        0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88,
        0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff, 0x00,
        0xab, 0xcd, 0xef, 0x01, 0x23, 0x45, 0x67, 0x89,
    ];

    // ===========================================
    // 2. ЗАПИСЬ БАЙТОВ В ФАЙЛ
    // ===========================================
    //
    // fs::write() может принимать не только строку, но и &[u8] (срез байтов).
    // Именно поэтому он называется "write", а не "write_string".
    //
    // 💡 ADVICE: Для бинарных данных используй расширение .bin
    // чтобы было понятно, что это не текст.
    fs::write("private_key.bin", &private_key)?;
    println!("✅ Приватный ключ сохранён в private_key.bin ({} байт)", private_key.len());

    // ===========================================
    // 3. ЧТЕНИЕ БАЙТОВ ИЗ ФАЙЛА
    // ===========================================
    //
    // fs::read() — читает файл целиком в Vec<u8>.
    // Аналог fs::read_to_string(), но возвращает байты, а не строку.
    let loaded_key = fs::read("private_key.bin")?;
    println!("📖 Прочитано {} байт из файла", loaded_key.len());

    // Мы знаем, что ключ — 32 байта. Превращаем Vec<u8> в [u8; 32].
    // Если размер не совпадёт — программа упадёт с сообщением.
    let loaded_key: [u8; 32] = loaded_key.try_into()
        .expect("Ожидалось 32 байта для приватного ключа");

    // Выводим hex-представление для проверки
    println!("   Ключ (hex): {}", hex_format(&loaded_key));

    // ===========================================
    // 4. ДОБАВЛЕНИЕ В КОНЕЦ БИНАРНОГО ФАЙЛА
    // ===========================================
    //
    // Создаём лог транзакций. Каждая запись — 8 байт.
    // Ты можешь использовать этот паттерн для логирования
    // всех транзакций кошелька.
    //
    // 💡 ADVICE: Бинарные логи занимают меньше места,
    // чем текстовые, и быстрее парсятся.
    // Но их сложнее читать человеку — используй для данных,
    // которые обрабатывает программа, а не человек.

    let mut log_file = fs::OpenOptions::new()
        .create(true)   // создать, если нет
        .append(true)   // добавлять в конец
        .open("transactions.bin")?;

    // Каждая "транзакция" — 8 байт: 4 байта ID + 4 байта сумма
    let tx1: [u8; 8] = [0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x64]; // ID=1, сумма=100
    let tx2: [u8; 8] = [0x00, 0x00, 0x00, 0x02, 0x00, 0x00, 0x00, 0x32]; // ID=2, сумма=50

    log_file.write_all(&tx1)?;
    log_file.write_all(&tx2)?;
    println!("➕ Добавлены 2 транзакции в лог");

    // Закрываем файл (выходим из области видимости)
    drop(log_file);

    // ===========================================
    // 5. ЧТЕНИЕ БИНАРНОГО ЛОГА
    // ===========================================
    let log_data = fs::read("transactions.bin")?;
    println!("📖 Лог содержит {} байт ({} транзакций)",
        log_data.len(),
        log_data.len() / 8);

    // Проходим по данным и извлекаем каждую транзакцию
    // chunks_exact(8) — разбивает слайс на куски по 8 байт
    for (i, chunk) in log_data.chunks_exact(8).enumerate() {
        // Первые 4 байта — ID (u32 big-endian)
        let id = u32::from_be_bytes([chunk[0], chunk[1], chunk[2], chunk[3]]);
        // Вторые 4 байта — сумма
        let amount = u32::from_be_bytes([chunk[4], chunk[5], chunk[6], chunk[7]]);
        println!("   Транзакция #{}: ID={}, сумма={}", i + 1, id, amount);
    }

    // ===========================================
    // 6. УБОРКА
    // ===========================================
    fs::remove_file("private_key.bin")?;
    fs::remove_file("transactions.bin")?;
    println!("🗑️ Временные файлы удалены");

    println!();
    println!("🎉 Урок 3 завершён! Ты научился:");
    println!("   - Записывать бинарные данные в файл");
    println!("   - Читать бинарные данные из файла");
    println!("   - Добавлять записи в бинарный лог");
    println!("   - Разбирать бинарные данные на части");

    Ok(())
}

/// Форматирует массив байт как hex-строку (для вывода)
fn hex_format(bytes: &[u8]) -> String {
    bytes.iter()
        .map(|b| format!("{:02x}", b))
        .collect::<Vec<_>>()
        .join(" ")
}
