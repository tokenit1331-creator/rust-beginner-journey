use clap::{Parser, Subcommand};

// ╔══════════════════════════════════════════════════════════════════╗
// ║  СУБКОМАНДЫ — ЭТО ГЛАВНАЯ ФИЧА ЛЮБОГО СЕРЬЁЗНОГО CLI           ║
// ║                                                                 ║
// ║  Посмотри на реальные примеры:                                  ║
// ║    cargo build       ← build — субкоманда                       ║
// ║    cargo run         ← run — субкоманда                         ║
// ║    cargo test        ← test — субкоманда                        ║
// ║    git commit        ← commit — субкоманда                      ║
// ║    git push          ← push — субкоманда                        ║
// ║    solana balance    ← balance — субкоманда                     ║
// ║    solana transfer   ← transfer — субкоманда                    ║
// ║                                                                 ║
// ║  Субкоманда — это КОМАНДА, которая идёт сразу после программы,  ║
// ║  и у неё могут быть СВОИ аргументы.                             ║
// ║                                                                 ║
// ║  Структура:                                                     ║
// ║    crypto-cli <СУБКОМАНДА> [аргументы субкоманды]               ║
// ║                                                                 ║
// ║  ПОЧЕМУ субкоманды?                                             ║
// ║  → Логическая группировка: balance, send, history — разные       ║
// ║    операции, у каждого свои аргументы                           ║
// ║  → --help покажет список всех команд                            ║
// ║  → Каждая субкоманда имеет свой --help                          ║
// ╚══════════════════════════════════════════════════════════════════╝

/// Крипто-кошелёк с субкомандами
#[derive(Parser)]
#[command(
    name = "crypto-wallet",
    version = "1.0.0",
    about = "Управление крипто-кошельком",
    // ╔════════════════════════════════════════════════════════════╗
    // ║  subcommand_required — если true, то без субкоманды       ║
    // ║  программа выведет help и завершится.                     ║
    // ║                                                           ║
    // ║  ПОЧЕМУ subcommand_required = true?                       ║
    // ║  В крипто-кошельке нет смысла запускать без команды.      ║
    // ║  Что делать — проверять баланс? Отправлять? Смотреть       ║
    // ║  историю? Без указания команды — clap сам подскажет.     ║
    // ╚════════════════════════════════════════════════════════════╝
    subcommand_required = true,
    arg_required_else_help = true,  // если нет аргументов — показать help
)]
struct Cli {
    /// Субкоманда: send, balance или history
    #[command(subcommand)]  // ← ключевой атрибут! Говорит clap:
    // "это поле — не обычный аргумент, а субкоманда"
    command: Commands,
}

// ╔══════════════════════════════════════════════════════════════════╗
// ║  ПЕРЕЧИСЛЕНИЕ СУБКОМАНД                                        ║
// ║                                                                 ║
// ║  Каждый вариант enum — это одна субкоманда.                     ║
// ║  У варианта могут быть свои поля — аргументы этой субкоманды.   ║
// ║                                                                 ║
// ║  ПОЧЕМУ enum, а не несколько структур?                          ║
// ║  → match по enum гарантирует, что мы обработали ВСЕ варианты    ║
// ║  → Новая субкоманда = новый вариант = ошибка компиляции,        ║
// ║    если не обработали                                          ║
// ║  → clap сам генерирует код для каждого варианта                ║
// ╚══════════════════════════════════════════════════════════════════╝
#[derive(Subcommand)]
enum Commands {
    // ╔══════════════════════════════════════════════════════════════╗
    // ║  СУБКОМАНДА: send — отправить транзакцию                   ║
    // ║                                                             ║
    // ║  Использование:                                             ║
    // ║    crypto-wallet send --to 0xabc --amount 0.5               ║
    // ║                                                             ║
    // ║  Атрибут about — описание для --help этой субкоманды         ║
    // ╚══════════════════════════════════════════════════════════════╝
    #[command(about = "Отправить криптовалюту на другой адрес")]
    Send {
        /// Адрес получателя
        #[arg(short, long, value_parser = parse_address)]
        to: String,

        /// Сумма отправки
        #[arg(short, long)]
        amount: f64,

        // ╔════════════════════════════════════════════════════════╗
        // ║  conflicts_with — КОНФЛИКТУЮЩИЕ АРГУМЕНТЫ             ║
        // ║                                                       ║
        // ║  Представь: пользователь написал                       ║
        // ║    send --all --amount 0.5                            ║
        // ║  Это бессмысленно! --all означает "отправить всё",     ║
        // ║  а --amount означает "отправить 0.5".                 ║
        // ║                                                       ║
        // ║  conflicts_with = "amount" — если указан --all,        ║
        // ║  то --amount НЕЛЬЗЯ указывать. И наоборот.           ║
        // ║                                                       ║
        // ║  ПОЧЕМУ conflicts_with в кавычках и по имени поля?    ║
        // ║  conflicts_with принимает строку с именем ДРУГОГО     ║
        // ║  аргумента в структуре. clap сам найдёт его и         ║
        // ║  настроит взаимное исключение.                       ║
        // ╚════════════════════════════════════════════════════════╝
        /// Отправить весь баланс
        #[arg(long, conflicts_with = "amount")]
        all: bool,

        /// Сеть назначения
        #[arg(short, long, default_value = "ethereum")]
        network: String,

        // ╔════════════════════════════════════════════════════════╗
        // ║  requires — ЗАВИСИМЫЕ АРГУМЕНТЫ                       ║
        // ║                                                       ║
        // ║  Если пользователь указал --gas-custom, он ОБЯЗАН     ║
        // ║  также указать --gas-price. Иначе транзакция          ║
        // ║  сломается — непонятно, какой газ использовать.       ║
        // ║                                                       ║
        // ║  ПОЧЕМУ это проверяет clap, а не мы в коде?           ║
        // ║  Потому что это ЛОГИКА CLI. Пользователь должен        ║
        // ║  узнать об ошибке ДО запуска программы, а не после.   ║
        // ╚════════════════════════════════════════════════════════╝
        /// Использовать кастомный газ
        #[arg(long, requires = "gas_price")]
        gas_custom: bool,

        /// Цена газа (в gwei)
        #[arg(long)]
        gas_price: Option<u64>,  // Option — необязательный аргумент

        // ╔════════════════════════════════════════════════════════╗
        // ║  num_args = 1.. — НЕСКОЛЬКО ЗНАЧЕНИЙ                  ║
        // ║                                                       ║
        // ║  tags: Vec<String> — пользователь может указать        ║
        // ║  --tags defi important срочно                         ║
        // ║  Все слова после --tags попадут в Vec.                ║
        // ║                                                       ║
        // ║  ПОЧЕМУ num_args = 1.., а не num_args = 3?            ║
        // ║  1.. означает "от 1 до бесконечности" — гибко.        ║
        // ║  Если указать 3 — можно только ровно 3 тега.          ║
        // ╚════════════════════════════════════════════════════════╝
        /// Теги для транзакции (можно несколько)
        #[arg(long, num_args = 1..)]
        tags: Vec<String>,
    },

    // ╔══════════════════════════════════════════════════════════════╗
    // ║  СУБКОМАНДА: balance — проверить баланс                    ║
    // ║                                                             ║
    // ║  Использование:                                             ║
    // ║    crypto-wallet balance --address 0xabc                    ║
    // ╚══════════════════════════════════════════════════════════════╝
    #[command(about = "Проверить баланс кошелька")]
    Balance {
        /// Адрес для проверки
        #[arg(short, long)]
        address: String,

        /// Сеть
        #[arg(short, long, default_value = "ethereum")]
        network: String,

        /// Подробный вывод
        #[arg(short, long)]
        verbose: bool,
    },

    // ╔══════════════════════════════════════════════════════════════╗
    // ║  СУБКОМАНДА: history — показать историю транзакций         ║
    // ║                                                             ║
    // ║  Использование:                                             ║
    // ║    crypto-wallet history --days 7 --verbose                 ║
    // ╚══════════════════════════════════════════════════════════════╝
    #[command(about = "Показать историю транзакций")]
    History {
        /// Количество дней истории
        #[arg(short, long, default_value_t = 30)]
        days: u32,

        /// Фильтр по типу: sent, received, all
        #[arg(short, long, default_value = "all")]
        filter: String,

        /// Подробный вывод
        #[arg(short, long)]
        verbose: bool,
    },
}


/// Кастомный парсер для адресов (такой же, как в 02)
fn parse_address(s: &str) -> Result<String, String> {
    if !s.starts_with("0x") {
        return Err(format!("Адрес должен начинаться с '0x'. Получено: {}", s));
    }
    let hex_part = &s[2..];
    if hex_part.len() != 40 {
        return Err(format!("Адрес должен быть 40 hex-символов. Получено: {}", hex_part.len()));
    }
    if !hex_part.chars().all(|c| c.is_ascii_hexdigit()) {
        return Err(format!("Адрес содержит не-шестнадцатеричные символы: {}", s));
    }
    Ok(s.to_lowercase())
}


fn main() {
    let cli = Cli::parse();

    // ╔════════════════════════════════════════════════════════════╗
    // ║  MATCH по субкомандам                                     ║
    // ║                                                           ║
    // ║  cli.command — это enum Commands. Мы сопоставляем его      ║
    // ║  с каждым вариантом и получаем ДОСТУП к аргументам         ║
    // ║  каждой субкоманды.                                       ║
    // ║                                                           ║
    // ║  Rust гарантирует: если мы добавим новую субкоманду,       ║
    // ║  компилятор заставит нас обработать её здесь.             ║
    // ╚════════════════════════════════════════════════════════════╝
    match &cli.command {
        Commands::Send { to, amount, all, network, gas_custom, gas_price, tags } => {
            println!("📤 Отправка транзакции");
            println!("━━━━━━━━━━━━━━━━━━━━");
            println!("  🎯 Получатель: {}", to);

            if *all {
                println!("  💰 Отправляется: ВЕСЬ БАЛАНС");
            } else {
                println!("  💰 Сумма:        {} ETH", amount);
            }

            println!("  🌐 Сеть:         {}", network);

            if *gas_custom {
                match gas_price {
                    Some(price) => println!("  ⛽ Газ (кастом): {} gwei", price),
                    None => println!("  ⛽ Газ: стандартный"),
                }
            } else {
                println!("  ⛽ Газ: стандартный (auto)");
            }

            if !tags.is_empty() {
                println!("  🏷️  Теги:         {}", tags.join(", "));
            }

            println!();
            println!("  ✅ Транзакция отправлена!");
            println!("  🔗 Хэш: 0x{}...ab12 (симуляция)", &to[2..6]);
        },
        Commands::Balance { address, network, verbose } => {
            println!("🪙 Проверка баланса");
            println!("━━━━━━━━━━━━━━━━━");
            println!("  📍 Адрес: {}", address);
            println!("  🌐 Сеть:  {}", network);

            if *verbose {
                println!("  🔍 RPC: https://{}.infura.io/v3/YOUR_KEY", network);
            }

            println!();
            println!("  💰 Баланс: 42.0 ETH (симуляция)");
        },
        Commands::History { days, filter, verbose } => {
            println!("📜 История транзакций");
            println!("━━━━━━━━━━━━━━━━━━━━");
            println!("  📅 Дней:     {}", days);
            println!("  🔍 Фильтр:   {}", filter);

            if *verbose {
                println!("  📊 Режим:    подробный");
                println!();
                println!("  ┌──────┬──────────────────────────────┬──────────┬────────┐");
                println!("  │ #    │ Хэш                          │ Сумма    │ Статус │");
                println!("  ├──────┼──────────────────────────────┼──────────┼────────┤");
                println!("  │ 1    │ 0xabc...def                  │ +0.5 ETH │ ✅     │");
                println!("  │ 2    │ 0x789...012                  │ -1.2 ETH │ ✅     │");
                println!("  └──────┴──────────────────────────────┴──────────┴────────┘");
            } else {
                println!("  📊 Найдено 2 транзакции");
                println!("  💡 Используй --verbose для деталей");
            }
        },
    }
}


