// ╔══════════════════════════════════════════════════════════════════╗
// ║  ПОЧЕМУ мы импортируем именно Parser, а не весь clap?          ║
// ║                                                                 ║
// ║  Parser — это типаж (trait). Когда мы пишем                     ║
// ║  #[derive(Parser)] для нашей структуры, компилятор              ║
// ║  автоматически генерирует код, который умеет:                   ║
// ║  1. Разбирать argv (аргументы командной строки)                 ║
// ║  2. Генерировать --help и --version                             ║
// ║  3. Проверять типы аргументов                                   ║
// ║                                                                 ║
// ║  Импортируем только то, что используем — это хорошая практика   ║
// ╚══════════════════════════════════════════════════════════════════╝
use clap::Parser;

// ╔══════════════════════════════════════════════════════════════════╗
// ║  СТРУКТУРА — сердце нашего CLI                                  ║
// ║                                                                 ║
// ║  Вся магия clap в том, что мы описываем «какие аргументы        ║
// ║  ожидаем» в виде обычной Rust-структуры. Каждое поле — это      ║
// ║  один аргумент командной строки.                                ║
// ║                                                                 ║
// ║  ПОЧЕМУ структура, а не функции?                                ║
// ║  → Rust-структуры — это данные. CLI-аргументы — это ТОЖЕ данные ║
// ║  → Компилятор проверяет типы: если поле String, то clap сам    ║
// ║    преобразует строку из терминала в Rust-строку                ║
// ║  → Вся конфигурация CLI в одном месте (читаемость!)             ║
// ╚══════════════════════════════════════════════════════════════════╝

/// 🔍 Документационный комментарий: он попадёт в --help
///
/// Крипто-кошелёк: приветствие и базовая информация
///
/// Первый шаг к созданию полноценного crypto CLI
///
/// ПОЧЕМУ три слеша ///, а не //?
/// Rust-документация (///) автоматически генерирует описание программы.
/// clap подхватывает эти комментарии и показывает в --help.
/// Попробуй: cargo run --example 01_hello_crypto -- --help
#[derive(Parser)]  // ← Вот она, магия! Генерирует парсер
#[command(         // ← Атрибуты для самой программы
    name = "crypto-wallet",       // Имя программы (показывается в help)
    version = "1.0.0",           // Версия (показывается в --version)
    author = "Rust Student",     // Автор
    about = "Учимся работать с крипто-кошельком через CLI"  // Описание
)]
struct Cli {
    // ╔══════════════════════════════════════════════════════════════╗
    // ║  ПОЗИЦИОННЫЙ АРГУМЕНТ                                       ║
    // ║                                                             ║
    // ║  Когда ты пишешь:                                           ║
    // ║    cargo run --example 01_hello_crypto -- 0x123abc          ║
    // ║                                       ^^^^^^^^              ║
    // ║  То "0x123abc" — это позиционный аргумент.                  ║
    // ║  Он не требует флага (--name), просто указывается           ║
    // ║  после имени программы.                                     ║
    // ║                                                             ║
    // ║  ПОЧЕМУ без атрибутов?                                      ║
    // ║  Если у поля нет #[arg(...)], clap считает его              ║
    // ║  ОБЯЗАТЕЛЬНЫМ позиционным аргументом.                       ║
    // ║  Имя поля (address) становится именем аргумента.            ║
    // ╚══════════════════════════════════════════════════════════════╝
    /// Адрес кошелька (обязательный аргумент)
    address: String,

    // ╔══════════════════════════════════════════════════════════════╗
    // ║  ОПЦИЯ С КОРОТКИМ ФЛАГОМ                                    ║
    // ║                                                             ║
    // ║  #[arg(short, long)] — это макрос-атрибут, который говорит:  ║
    // ║  "Сделай для этого поля флаги -n (short) и --network (long)"
    // ║                                                             ║
    // ║  short = 'n' — какой символ использовать для короткого флага║
    // ║  Если написать просто short, то clap сам возьмёт ПЕРВУЮ     ║
    // ║  букву имени поля (network → -n)                            ║
    // ║                                                             ║
    // ║  ПОЧЕМУ мы указали short = 'n' явно, а не "просто short"?   ║
    // ║  Потому что мы ЯВНО хотим, чтобы было -n, а не -e.          ║
    // ║  short без значения = первая буква поля → -n                ║
    // ║  Так что можно было написать просто short. Но я показал      ║
    // ║  явный вариант, чтобы ты знал, что можно задать ЛЮБУЮ букву.║
    // ╚══════════════════════════════════════════════════════════════╝
    /// Сеть: ethereum, polygon, solana
    #[arg(short = 'n', long = "network", default_value = "ethereum")]
    network: String,

    // ╔══════════════════════════════════════════════════════════════╗
    // ║  ФЛАГ (тип bool)                                            ║
    // ║                                                             ║
    // ║  Флаг — это аргумент, который либо есть, либо нет.          ║
    // ║  Он НЕ принимает значение. Просто:                           ║
    // ║    cargo run --example 01_hello_crypto -- 0x123 --verbose    ║
    // ║                                       ^^^^^^^               ║
    // ║  — если флаг есть → поле verbose = true                     ║
    // ║  — если флага нет → поле verbose = false                    ║
    // ║                                                             ║
    // ║  ПОЧЕМУ именно bool?                                        ║
    // ║  clap смотрит на тип поля. bool = флаг. String = опция.     ║
    // ║  Вот почему типы в Rust так важны — они УПРАВЛЯЮТ           ║
    // ║  поведением библиотеки!                                     ║
    // ╚══════════════════════════════════════════════════════════════╝
    /// Подробный вывод (флаг)
    #[arg(short, long)]  // short без значения = первая буква поля
    verbose: bool,
}

// ╔══════════════════════════════════════════════════════════════════╗
// ║  ФУНКЦИЯ main — точка входа                                    ║
// ║                                                                 ║
// ║  Здесь происходит ВСЯ работа:                                   ║
// ║  1. Cli::parse() разбирает argv                                 ║
// ║  2. Если ошибка → clap сам выводит ошибку и завершает программу ║
// ║  3. Если --help → clap выводит help и завершает                 ║
// ║  4. Если всё ок → получаем структуру с данными                  ║
// ╚══════════════════════════════════════════════════════════════════╝
fn main() {
    // ╔════════════════════════════════════════════════════════════╗
    // ║  Cli::parse() — ЕДИНСТВЕННЫЙ вызов, который делает всё    ║
    // ║                                                           ║
    // ║  Что происходит под капотом:                               ║
    // ║  1. std::env::args_os() — читает аргументы терминала       ║
    // ║  2. clap разбирает их по правилам из структуры             ║
    // ║  3. Заполняет поля структуры                               ║
    // ║  4. Возвращает готовую Cli                                 ║
    // ║                                                           ║
    // ║  Если что-то пошло не так (не хватает аргумента,           ║
    // ║  неправильный тип) — программа завершится с ошибкой        ║
    // ║  и clap покажет КРАСИВОЕ сообщение.                       ║
    // ╚════════════════════════════════════════════════════════════╝
    let cli = Cli::parse();

    // ╔════════════════════════════════════════════════════════════╗
    // ║  Теперь cli — это обычная Rust-структура!                 ║
    // ║  Обращаемся к полям через точку:                          ║
    // ║    cli.address  → "0x123abc"                               ║
    // ║    cli.network  → "ethereum" (или то, что передал user)   ║
    // ║    cli.verbose  → true или false                          ║
    // ╚════════════════════════════════════════════════════════════╝
    println!("🪙 Крипто-кошелёк");
    println!("━━━━━━━━━━━━━━━━");

    // Выводим адрес — он всегда есть, потому что обязательный
    println!("📍 Адрес: {}", cli.address);

    // Выводим сеть — она всегда есть, потому что есть default_value
    println!("🌐 Сеть:  {}", cli.network);

    // ╔════════════════════════════════════════════════════════════╗
    // ║  ПОЧЕМУ у network есть default_value?                     ║
    // ║                                                           ║
    // ║  Представь: пользователь каждый раз пишет --network        ║
    // ║  ethereum. Это раздражает. 90% пользователей работают     ║
    // ║  с Ethereum, поэтому мы сделали его значением по умолчанию.║
    // ║  Пользователь может его переопределить, если нужно.       ║
    // ║                                                           ║
    // ║  В реальных проектах (solana-cli) тоже есть default:      ║
    // ║  --url по умолчанию localhost, но можно переключить на     ║
    // ║  mainnet или testnet.                                     ║
    // ╚════════════════════════════════════════════════════════════╝

    // ╔════════════════════════════════════════════════════════════╗
    // ║  УСЛОВНАЯ ЛОГИКА с флагом verbose                         ║
    // ║                                                           ║
    // ║  cli.verbose — это просто bool. Мы проверяем его как       ║
    // ║  обычное условие. Если пользователь передал --verbose,     ║
    // ║  то выводим дополнительную информацию.                    ║
    // ║                                                           ║
    // ║  В реальных проектах (например, cargo build -v) verbose    ║
    // ║  включает подробные логи, трассировку, тайминги.          ║
    // ╚════════════════════════════════════════════════════════════╝
    if cli.verbose {
        println!("🔍 Режим: подробный");
        println!("📋 Полученные аргументы:");
        println!("   address = '{}'", cli.address);
        println!("   network = '{}'", cli.network);
        println!("   verbose = {}", cli.verbose);
    }

    println!("━━━━━━━━━━━━━━━━");
    println!("✅ Готово! Кошелёк инициализирован.");
}


