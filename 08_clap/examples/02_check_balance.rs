// ║  ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ                                       ║
// ║                                                                 ║
// ║  Здесь мы видим ИДИОМУ Rust: сопоставление с образцом (match).  ║
// ║  Каждый вариант enum обрабатывается отдельно.                   ║
// ║  Компилятор проверяет, что мы обработали ВСЕ варианты.         ║
// ╚══════════════════════════════════════════════════════════════════╝

/// Возвращает числовой ID сети (chain_id)
fn network_id(network: &Network) -> u32 {
    match network {
        Network::Ethereum => 1,      // mainnet
        Network::Polygon => 137,
        Network::Solana => 101,      // условно
        Network::Arbitrum => 42161,
        Network::Optimism => 10,
    }
}

/// Возвращает URL RPC-ноды для сети
fn get_rpc_url(network: &Network) -> &str {
    match network {
        Network::Ethereum => "https://eth-mainnet.g.alchemy.com/v2/",
        Network::Polygon => "https://polygon-mainnet.g.alchemy.com/v2/",
        Network::Solana => "https://api.mainnet-beta.solana.com",
        Network::Arbitrum => "https://arb-mainnet.g.alchemy.com/v2/",
        Network::Optimism => "https://opt-mainnet.g.alchemy.com/v2/",
    }
}


