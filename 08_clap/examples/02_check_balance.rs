use clap::Parser;

// ╔══════════════════════════════════════════════════════════════════╗
// ║  ValueEnum — ПЕРЕЧИСЛЕНИЕ ДОПУСТИМЫХ ЗНАЧЕНИЙ                   ║
// ║                                                                 ║
// ║  Представь: мы хотим, чтобы пользователь выбирал ТОЛЬКО         ║
// ║  из нескольких вариантов сети. Нельзя вписать "bitcoin" если    ║
// ║  мы поддерживаем только ethereum, polygon, solana.              ║
// ║                                                                 ║
// ║  #[derive(ValueEnum)] — это атрибут clap, который:              ║
// ║  1. Автоматически создаёт парсер для вариантов                  ║
// ║  2. Если пользователь введёт что-то другое → clap покажет       ║
// ║     ошибку с ВСЕМИ допустимыми вариантами                      ║
// ║  3. Регистронезависимый: Ethereum, ethereum, ETHEREUM — всё OK  ║
// ║                                                                 ║
// ║  ПОЧЕМУ ValueEnum, а не просто String?                          ║
// ║  → Безопасность: пользователь не передаст "bitcoin"             ║
// ║  → Удобство: clap сам покажет варианты в --help                 ║
// ║  → Производительность: сравнение enum-дискриминанта быстрее     ║
// ║    сравнения строк                                              ║
// ╚══════════════════════════════════════════════════════════════════╝
#[derive(clap::ValueEnum, Clone)]
// ПОЧЕМУ Clone? Потому что мы будем передавать значение в функцию
// парсинга, а она забирает владение. Clone позволяет копировать.
enum Network {
    Ethereum,
    Polygon,
    Solana,
    Arbitrum,
    Optimism,
}

// ╔══════════════════════════════════════════════════════════════════╗
// ║  fmt::Display для Network — как показывать пользователю         ║
// ║                                                                 ║
// ║  Когда мы выводим println!("{}", network), Rust ищет типаж     ║
// ║  Display. Если его нет — ошибка компиляции. Поэтому мы          ║
// ║  реализуем Display для Network.                                 ║
// ║                                                                 ║
// ║  ПОЧЕМУ мы сами пишем Display, а не используем derive?          ║
// ║  derive(Debug) выводит "Ethereum". Display выведет "ethereum"   ║
// ║  (с маленькой буквы — красивее для CLI).                        ║
// ╚══════════════════════════════════════════════════════════════════╝
impl std::fmt::Display for Network {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Network::Ethereum => write!(f, "ethereum"),
            Network::Polygon => write!(f, "polygon"),
            Network::Solana => write!(f, "solana"),
            Network::Arbitrum => write!(f, "arbitrum"),
            Network::Optimism => write!(f, "optimism"),
        }
    }
}


// ╔══════════════════════════════════════════════════════════════════╗
// ║  КАСТОМНЫЙ ПАРСЕР АДРЕСА                                       ║
// ║                                                                 ║
// ║  Стандартный парсер clap просто принимает любую строку.         ║
// ║  Но адрес крипто-кошелька имеет ФОРМАТ:                        ║
// ║    - Ethereum: 0x + 40 hex символов                             ║
// ║    - Solana:   32-44 base58 символа                             ║
// ║                                                                 ║
// ║  Мы хотим проверять формат адреса ПРИ ПАРСИНГЕ, а не потом.     ║
// ║  Это называется "fail fast" — ошибка должна произойти          ║
// ║  как можно раньше.                                             ║
// ║                                                                 ║
// ║  Функция-парсер принимает &str (сырую строку из терминала)     ║
// ║  и возвращает Result<String, String> (ошибка или готовое        ║
// ║  значение).                                                     ║
// ║                                                                 ║
// ║  ПОЧЕМУ возвращаем String, а не &str?                           ║
// ║  Потому что &str — это ссылка на данные, которыми владеет       ║
// ║  кто-то другой. А нам нужно отдать значение структуре, которая  ║
// ║  будет жить дальше. Rust не позволит — пришлось бы             ║
// ║  разбираться с lifetime. Проще вернуть String (владеет данными).║
// ╚══════════════════════════════════════════════════════════════════╝
fn parse_address(s: &str) -> Result<String, String> {
    // ╔════════════════════════════════════════════════════════════╗
    // ║  Проверка префикса 0x                                     ║
    // ║  Адреса Ethereum/Solana часто начинаются с 0x             ║
    // ╚════════════════════════════════════════════════════════════╝
    if !s.starts_with("0x") {
        return Err(format!(
            "❌ Адрес должен начинаться с '0x'. Получено: {}",
            s
        ));
    }

    // ╔════════════════════════════════════════════════════════════╗
    // ║  Берём часть после 0x и проверяем, что это hex           ║
    // ║  hex = 0-9 и a-f (или A-F)                               ║
    // ╚════════════════════════════════════════════════════════════╝
    let hex_part = &s[2..];  // отрезаем "0x"

    // ╔════════════════════════════════════════════════════════════╗
    // ║  Длина адреса: 40 hex символов = 20 байт = 160 бит       ║
    // ║  Это стандарт Ethereum (160-битный адрес).                ║
    // ╚════════════════════════════════════════════════════════════╝
    if hex_part.len() != 40 {
        return Err(format!(
            "❌ Адрес должен быть ровно 40 hex-символов (20 байт). \
             Получено: {} символов",
            hex_part.len()
        ));
    }

    // ╔════════════════════════════════════════════════════════════╗
    // ║  Проверяем каждый символ — это hex-цифра?                 ║
    // ║  chars().all(...) — вернёт true, если ВСЕ символы         ║
    // ║  проходят проверку is_ascii_hexdigit()                    ║
    // ║                                                           ║
    // ║  ПОЧЕМУ is_ascii_hexdigit, а не своя проверка?            ║
    // ║  Это стандартный метод Rust, быстрый, проверенный         ║
    // ║  миллионами проектов. Не изобретай велосипед.             ║
    // ╚════════════════════════════════════════════════════════════╝
    if !hex_part.chars().all(|c| c.is_ascii_hexdigit()) {
        return Err(format!(
            "❌ Адрес содержит не-шестнадцатеричные символы: {}",
            s
        ));
    }

    // ╔════════════════════════════════════════════════════════════╗
    // ║  Если всё ок — возвращаем адрес в нижнем регистре         ║
    // ║  to_lowercase() — нормализуем: 0xABC и 0xabc — одно и то же║
    // ║                                                           ║
    // ║  ПОЧЕМУ нормализуем в lowercase?                          ║
    // ║  В крипте адреса регистронезависимы (EIP-55 — это         ║
    // ║  только для чексуммы, хранение в нижнем регистре — норма).║
    // ╚════════════════════════════════════════════════════════════╝
    Ok(s.to_lowercase())
}


// ╔══════════════════════════════════════════════════════════════════╗
// ║  СТРУКТУРА CLI с продвинутыми атрибутами                       ║
// ║                                                                 ║
// ║  Теперь мы используем:                                          ║
// ║  • value_parser — кастомная проверка                            ║
// ║  • default_value_t — значение по умолчанию из Rust-значения     ║
// ║  • value_enum — строгий набор значений                          ║
// ╚══════════════════════════════════════════════════════════════════╝
#[derive(Parser)]
#[command(
    name = "check-balance",
    version = "1.0.0",
    about = "Проверка баланса крипто-кошелька"
)]
struct Cli {
    /// Адрес кошелька (с кастомной валидацией!)
    ///
    /// ПОЧЕМУ value_parser, а не просто String?
    /// Без value_parser: clap принимает любую строку
    /// С value_parser: clap вызывает нашу функцию parse_address
    ///   Если она вернёт Err — clap сам покажет ошибку и завершится
    ///   Если Ok — сохранит значение в поле address
    ///
    /// Это называется "валидация на границе системы" (parse, don't validate).
    /// Мы проверяем данные ПРИ ВХОДЕ, а не в середине программы.
    #[arg(value_parser = parse_address)]
    address: String,

    /// Сеть (только из списка!)
    ///
    /// ПОЧЕМУ value_enum + default_value_t, а не default_value?
    /// default_value = "ethereum" — это строка, clap распарсит её
    /// default_value_t = Network::Ethereum — это Rust-значение
    /// Разница: второй вариант проверяется КОМПИЛЯТОРОМ.
    /// Если мы опечатаемся в строке "ethereum" — ошибка только в
    /// рантайме. Если передадим неверный enum — ошибка при компиляции.
    #[arg(short, long, value_enum, default_value_t = Network::Ethereum)]
    network: Network,

    /// Подтверждения сети (число, от 1 до 100)
    ///
    /// ПОЧЕМУ .range(1..=100)?
    /// Представь: пользователь запросил 999 подтверждений.
    /// Это бессмысленно — блоки не подтверждаются 999 раз.
    /// range проверяет ДОПУСТИМЫЙ ДИАПАЗОН при парсинге.
    #[arg(short, long, default_value_t = 12, value_parser = clap::value_parser!(u32).range(1..=100))]
    confirmations: u32,

    /// Подробный вывод
    #[arg(short, long)]
    verbose: bool,
}


fn main() {
    let cli = Cli::parse();

    println!("🪙 Проверка баланса");
    println!("━━━━━━━━━━━━━━━━━━");

    // ╔════════════════════════════════════════════════════════════╗
    // ║  Обрати внимание: address УЖЕ ПРОВЕРЕН!                   ║
    // ║  Если бы пользователь передал "abc" (без 0x), программа   ║
    // ║  даже бы не запустилась — clap вывел бы ошибку.           ║
    // ╚════════════════════════════════════════════════════════════╝
    println!("📍 Адрес:     {}", cli.address);
    println!("🌐 Сеть:      {} (id: {})", cli.network, network_id(&cli.network));
    println!("✅ Подтверждений: минимум {}", cli.confirmations);

    if cli.verbose {
        println!("");
        println!("🔍 Детали запроса:");
        println!("   Полный адрес:    {}", cli.address);
        println!("   Длина адреса:    {} символов", cli.address.len());
        println!("   Префикс:         {}", &cli.address[..2]);
        println!("   Хвост:           ..{} (последние 4 символа)", &cli.address[cli.address.len()-4..]);
        println!("   Сеть:            {}", cli.network);
        println!("   Подтверждения:   {}", cli.confirmations);
        println!("   Режим:           подробный");
    }

    // ╔════════════════════════════════════════════════════════════╗
    // ║  Симуляция запроса к блокчейну                             ║
    // ║  В реальном проекте здесь был бы RPC-вызов:                ║
    // ║    let balance = provider.get_balance(&address).await;      ║
    // ╚════════════════════════════════════════════════════════════╝
    println!();
    println!("📊 Имитация запроса баланса...");
    println!("   → отправлен RPC-запрос к {}", get_rpc_url(&cli.network));
    println!("   → ожидание {} подтверждений", cli.confirmations);
    println!("   ✓ Баланс: 1.42 ETH");

    println!("━━━━━━━━━━━━━━━━━━");
    println!("✅ Баланс получен.");
}


