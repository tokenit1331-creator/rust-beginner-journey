// ============================================================
// ПРИМЕР 6: ПРОДВИНУТЫЕ ФИЧИ clap
// ============================================================
//   - Аргументы из переменных окружения (env)
//   - Автодополнение (shell completion)
//   - Цветной вывод и кастомизация help
//   - Multiple occurrences (-vvv)
//   - Скрытые аргументы
// ============================================================

use clap::Parser;

/// Демонстрация продвинутых возможностей
#[derive(Parser)]
#[command(
    name = "advanced",
    version = "1.0.0",
    // Кастомный help template
    help_template = r#"{before-help}
╔══════════════════════════════════════════╗
║  {name} v{version}                          ║
║  {about}                                  ║
╚══════════════════════════════════════════╝

{usage-heading} {usage}

{all-args}{after-help}

📌 Документация: https://docs.rs/clap
"#,
    // Отключаем цветной help (по умолчанию включён в clap 4.5+)
    disable_colored_help = false,
)]
struct Args {
    // ============================================================
    // ФИЧА 1: Переменные окружения (env)
    // ============================================================
    // Можно задать значение через переменную окружения
    // если аргумент не передан в командной строке

    /// API ключ (можно указать через API_KEY=... или --api-key)
    #[arg(
        short, long,
        env = "API_KEY",
        hide_env_values = true,  // не показывать значение в --help
    )]
    api_key: String,

    // ============================================================
    // ФИЧА 2: Множественные повторы (count)
    // ============================================================
    // -v = 1, -vv = 2, -vvv = 3 и т.д.

    /// Уровень детализации (-v, -vv, -vvv)
    #[arg(short, long, action = clap::ArgAction::Count)]
    verbose: u8,

    // ============================================================
    // ФИЧА 3: Скрытые аргументы (не показываются в help)
    // ============================================================

    /// Секретный режим (не показывается в --help)
    #[arg(short, long, hide = true)]
    secret: bool,

    // ============================================================
    // ФИЧА 4: Кастомное действие (set / append / count)
    // ============================================================

    /// Игнорировать предупреждения
    #[arg(short = 'w', long, action = clap::ArgAction::SetTrue)]
    nowarn: bool,

    // ============================================================
    // ФИЧА 5: Разделитель значений
    // ============================================================

    /// Список тегов (можно через запятую: --tags a,b,c)
    #[arg(short, long, value_delimiter = ',', num_args = 1..)]
    tags: Vec<String>,

    // ============================================================
    // ФИЧА 6: Отображаемое значение по умолчанию
    // ============================================================

    /// Формат вывода
    #[arg(short, long, default_value = "json", default_missing_value = "json", value_parser = ["json", "yaml", "toml"])]
    format: String,
}

fn main() {
    let args = Args::parse();

    println!("🔑 API ключ: {}...{}", 
        &args.api_key[..3.min(args.api_key.len())],
        &args.api_key[args.api_key.len().saturating_sub(3)..]
    );
    println!("🔊 Уровень лога: {}", args.verbose);
    println!("🏷️  Теги: {:?}", args.tags);
    println!("📄 Формат: {}", args.format);

    if args.secret {
        println!("🕵️ Секретный режим АКТИВИРОВАН!");
    }
    if args.nowarn {
        println!("⚠️ Предупреждения отключены");
    }
}

