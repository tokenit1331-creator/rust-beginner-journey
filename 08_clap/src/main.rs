// ============================================================
// ГЛАВНЫЙ ФАЙЛ: Все базовые возможности clap в одном месте
// ============================================================
// clap (Command Line Argument Parser) — стандарт парсинга CLI в Rust
//
// Два подхода:
//   1. Derive (макросы)     — 🪄 современный, рекомендуемый
//   2. Builder (без макросов) — 🔨 гибкий, для сложных случаев
//
// Устанавливаем: cargo add clap --features derive
// ============================================================

use clap::Parser;

/// 📌 Документация к структуре = описание программы (--help)
/// clap сам сгенерирует help, version, usage
#[derive(Parser)]
#[command(
    name = "demo",              // имя программы
    version = "1.0.0",          // --version покажет это
    author = "Rust Mentor",     // автор
    about = "Демонстрация всех базовых возможностей clap",
    long_about = "Это полная демонстрация того, как работает clap.\nТут есть позиционные аргументы, опции, флаги, значения по умолчанию.",
    after_help = "📖 Пример: cargo run -- Артём -n 3 --formal --language ru",
    next_line_help = true       // каждый аргумент на новой строке
)]
struct Args {
    // ============================================================
    // 1️⃣ ПОЗИЦИОННЫЕ АРГУМЕНТЫ (обязательные, если не указано иное)
    // ============================================================

    /// Имя пользователя — первый аргумент без флага
    /// Позиционные аргументы идут в порядке объявления полей
    name: String,

    // ============================================================
    // 2️⃣ ОПЦИИ (--флаг со значением)
    // ============================================================

    /// Количество повторений (--count или -n)
    /// short — короткий флаг, long — полный
    /// default_value_t — значение по умолчанию, если флаг не указан
    #[arg(short = 'n', long = "count", default_value_t = 1)]
    count: u32,

    /// Язык приветствия (--language или -l)
    /// Значение по умолчанию — строка "ru"
    #[arg(short = 'l', long = "language", default_value = "ru")]
    language: String,

    // ============================================================
    // 3️⃣ ФЛАГИ (bool — true если флаг указан, false если нет)
    // ============================================================

    /// Официальное приветствие (--formal / -f)
    /// short/long без буквы = clap сам подставит первую букву
    #[arg(short, long)]
    formal: bool,

    /// Подробный вывод (--verbose / -v)
    #[arg(short, long)]
    verbose: bool,

    // ============================================================
    // 4️⃣ ОПЦИИ С ВАЛИДАЦИЕЙ (value_parser)
    // ============================================================

    /// Число ответов — проверяем, что в диапазоне 1..=10
    /// value_parser — встроенная или кастомная валидация
    #[arg(short = 'r', long = "replies", default_value_t = 1, value_parser = clap::value_parser!(u32).range(1..=10))]
    replies: u32,

    // ============================================================
    // 5️⃣ НЕСКОЛЬКО ЗНАЧЕНИЙ (Vec<T>)
    // ============================================================

    /// Список интересов — можно указать несколько: -i код -i спорт
    /// num_args = 1.. — минимум 1, максимум безлимит
    #[arg(short = 'i', long = "interest", num_args = 1..)]
    interests: Vec<String>,

    // ============================================================
    // 6️⃣ ДОПОЛНИТЕЛЬНЫЕ АРГУМЕНТЫ (всё что осталось)
    // ============================================================

    /// Файлы для обработки (все позиционные после --)
    /// trailing_var_arg — всё что после "--" идёт сюда
    #[arg(last = true)]
    files: Vec<String>,
}

fn main() {
    // 👇 ОДНА СТРОКА — и clap сам разобрал все аргументы!
    let args = Args::parse();

    // Используем полученные данные
    let greeting = match args.formal {
        true if args.language == "ru" => format!("Здравствуйте, {}!", args.name),
        false if args.language == "ru" => format!("Привет, {}!", args.name),
        true => format!("Hello, {}!", args.name),
        false => format!("Hey, {}!", args.name),
    };

    for _ in 0..args.count {
        if args.verbose {
            println!("[INFO] Печатаю приветствие №{} для {}", _, args.name);
        }
        println!("{}", greeting);
    }

    if !args.interests.is_empty() {
        println!("\nВаши интересы: {}", args.interests.join(", "));
    }

    if !args.files.is_empty() {
        println!("\nФайлы для обработки: {:?}", args.files);
    }

    println!("\nОтветов ожидается: {}", args.replies);
}
