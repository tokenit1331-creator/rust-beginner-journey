// ============================================================
// 🔵 Шаг 1: Базовые итераторы
// Тема: iter(), into_iter(), iter_mut(), for, while
// Контекст: Обход списка крипто-монет
// ============================================================

/// Структура монеты в портфеле
#[derive(Debug, Clone)]
struct Coin {
    name: String,
    amount: f64,
    price_usd: f64,
}

fn main() {
    // Создаём портфель из 3 монет (как Vec структур)
    let portfolio = vec![
        Coin { name: "Bitcoin".to_string(),  amount: 0.5,  price_usd: 67000.0 },
        Coin { name: "Ethereum".to_string(), amount: 5.0,  price_usd: 3400.0  },
        Coin { name: "Solana".to_string(),   amount: 20.0, price_usd: 150.0   },
    ];

    // ============================================================
    // 1️⃣ iter() — итератор по ССЫЛКАМ (&T)
    //
    // 💡 ADVICE: Используй iter(), когда тебе нужно только
    // прочитать данные, не забирая владение. Это самый
    // безопасный и часто используемый способ.
    // После iter() оригинальный Vec всё ещё доступен!
    // ============================================================
    println!("=== 1. iter() — итератор по ссылкам ===");

    // iter() возвращает ссылки на элементы
    // Тип item: &Coin
    let mut iter = portfolio.iter();

    // next() возвращает Option<&Coin>
    // Some(&coin) — если есть элемент
    // None — если перебор закончен
    while let Some(coin) = iter.next() {
        println!("  Монета: {} | Кол-во: {} | Цена: ${}", coin.name, coin.amount, coin.price_usd);
    }

    // ✅ portfolio всё ещё доступен, потому что iter() брал ссылки
    println!("  (портфель всё ещё доступен: {} монет)\n", portfolio.len());

    // ============================================================
    // 2️⃣ into_iter() — итератор с ЗАБИРАНИЕМ ВЛАДЕНИЯ (T)
    //
    // 💡 ADVICE: Используй into_iter(), когда данные больше
    // не нужны после перебора. Это забирает владение —
    // после цикла исходный Vec НЕДОСТУПЕН.
    // ============================================================
    println!("=== 2. into_iter() — итератор с владением ===");

    // Создаём копию, чтобы portfolio остался жив
    let portfolio_copy = portfolio.clone();

    // into_iter() забирает владение
    // Тип item: Coin (не &Coin, а сам Coin)
    for coin in portfolio_copy.into_iter() {
        println!("  Владею: {} ({} шт.)", coin.name, coin.amount);
    }

    // ❌ portfolio_copy больше недоступен!
    // Раскомментируй — будет ошибка компиляции:
    // println!("{}", portfolio_copy.len());

    // ✅ Но оригинальный portfolio жив!
    println!("  (оригинал цел: {} монет)\n", portfolio.len());

    // ============================================================
    // 3️⃣ iter_mut() — итератор по МУТАБЕЛЬНЫМ ссылкам (&mut T)
    //
    // 💡 ADVICE: Используй iter_mut(), когда нужно ИЗМЕНИТЬ
    // элементы на месте. Например, обновить цену или баланс.
    // ============================================================
    println!("=== 3. iter_mut() — мутабельные ссылки ===");

    // Создаём мутабельную копию
    let mut portfolio_mut = portfolio.clone();

    // iter_mut() даёт &mut Coin — можно менять поля
    for coin in portfolio_mut.iter_mut() {
        // Добавляем бонус 10% к каждой монете
        coin.amount *= 1.10;
    }

    // Проверяем, что цены обновились
    for coin in &portfolio_mut {
        println!("  {}: {:.2} шт. (было {:.2})", coin.name, coin.amount, 
                 portfolio.iter().find(|c| c.name == coin.name).unwrap().amount);
    }

    // ============================================================
    // 4️⃣ for vs while — синтаксический сахар
    //
    // 💡 ADVICE: Используй for — он короче и безопаснее.
    // while с next() — это то же самое, но больше кода.
    // Под капотом for вызывает into_iter().
    // ============================================================
    println!("\n=== 4. for vs while ===");

    // for — это синтаксический сахар над while let + iter.next()
    // Вот что делает компилятор, когда ты пишешь for:
    // 1. Вызывает vec.into_iter()
    // 2. В цикле вызывает iter.next()
    // 3. Если Some(x) — выполняет тело, если None — выходит

    // Этот код:
    println!("  for (кратко):");
    for coin in &portfolio {
        println!("    {} — ${:.2}", coin.name, coin.amount * coin.price_usd);
    }

    // Компилируется в то же самое, что:
    println!("  while (подробно):");
    let mut iter = portfolio.iter();
    while let Some(coin) = iter.next() {
        println!("    {} — ${:.2}", coin.name, coin.amount * coin.price_usd);
    }

    // ============================================================
    // 5️⃣ Срез (slice) тоже даёт итератор
    //
    // 💡 ADVICE: Массивы и срезы — тоже итерируемые.
    // У них есть .iter(), .iter_mut(), и они работают в for.
    // ============================================================
    println!("\n=== 5. Итерация по срезу ===");

    let prices = [67000.0, 3400.0, 150.0];

    for (i, price) in prices.iter().enumerate() {
        println!("  Монета {}: ${}", i + 1, price);
    }

    // ============================================================
    // 🧠 ИТОГ:
    // iter()      — читаем данные (&T)      — Vec остаётся
    // into_iter() — забираем владение (T)   — Vec уходит
    // iter_mut()  — изменяем данные (&mut T) — Vec остаётся
    // for         — синтаксический сахар    — вызывает into_iter()
    // ============================================================
    println!("\n✅ Итог: iter() — читать, into_iter() — забрать, iter_mut() — менять");
}
