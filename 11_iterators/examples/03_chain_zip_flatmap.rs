// ============================================================
// 🟠 Шаг 3: chain, zip, flat_map, rev, step_by
// Тема: Объединение и комбинирование итераторов
// Контекст: Объединение портфелей, пары цена-количество
// ============================================================

fn main() {
    // ============================================================
    // 1️⃣ chain() — склеиваем два итератора в один
    //
    // 💡 ADVICE: chain() полезен, когда данные разбиты
    // на две части, но обрабатывать нужно как одно целое.
    // Например, портфели на двух биржах.
    // ============================================================
    println!("=== 1. chain() — склеивание итераторов ===");

    let binance = vec!["BTC", "ETH", "SOL"];
    let kraken  = vec!["ADA", "DOT"];

    // chain() склеивает два итератора последовательно
    // Сначала все элементы из первого, потом из второго
    let all_exchanges: Vec<&&str> = binance.iter()
        .chain(kraken.iter())
        .collect();

    println!("  Все монеты с двух бирж: {:?}", all_exchanges);

    // chain() можно вкладывать
    let coinbase = vec!["AVAX", "LINK"];
    let all_three: Vec<&&str> = binance.iter()
        .chain(kraken.iter())
        .chain(coinbase.iter())
        .collect();

    println!("  С трёх бирж: {:?}", all_three);

    // ============================================================
    // 2️⃣ zip() — спариваем два итератора поэлементно
    //
    // 💡 ADVICE: zip() создаёт пары (a, b) из двух итераторов.
    // Останавливается на КОРОТКОМ из двух — если один
    // итератор короче, лишние элементы второго отбрасываются.
    // ============================================================
    println!("\n=== 2. zip() — спаривание итераторов ===");

    let coins = vec!["BTC", "ETH", "SOL", "ADA"];
    let prices = vec![67000.0, 3400.0, 150.0];  // короче!

    // zip() создаёт кортежи (coin, price)
    // SOL есть, а ADA — нет, потому что prices кончились
    let with_prices: Vec<(&str, &f64)> = coins.iter()
        .zip(prices.iter())  // спариваем поэлементно
        .collect();

    for (coin, price) in &with_prices {
        println!("  {}: ${}", coin, price);
    }
    println!("  (ADA отброшен — prices короче)");

    // Реальный кейс: баланс кошелька
    let amounts = vec![0.5, 5.0, 20.0];
    let portfolio_value: Vec<(&str, f64)> = coins.iter()
        .zip(prices.iter())
        .zip(amounts.iter())
        .map(|((coin, price), amount)| (*coin, price * amount))
        .collect();
    //      ^^^^^^^^^^^^^^^^^^^^^^^^
    //  раскрываем вложенные кортежи

    println!("\n  Стоимость портфеля:");
    for (coin, value) in &portfolio_value {
        println!("    {}: ${:.2}", coin, value);
    }

    // ============================================================
    // 3️⃣ flat_map() — разворачиваем вложенные структуры
    //
    // 💡 ADVICE: flat_map() = map() + flatten().
    // Каждый элемент превращается в итератор, и все эти
    // итераторы склеиваются в один.
    // Идеально для Vec<Vec<T>> или Option<Vec<T>>.
    // ============================================================
    println!("\n=== 3. flat_map() — разворачивание ===");

    // У каждой биржи — список монет
    let exchange_coins = vec![
        vec!["BTC", "ETH"],           // Binance
        vec!["SOL", "ADA", "DOT"],    // Kraken
        vec!["AVAX"],                 // Coinbase
    ];

    // flat_map применяет closure к каждому элементу
    // closure возвращает итератор (здесь .iter())
    // flat_map склеивает все итераторы в один
    let all_coins: Vec<&str> = exchange_coins.iter()
        .flat_map(|exchange| exchange.iter())  // разворачиваем
        .map(|&c| c)                           // разыменовываем
        .collect();

    println!("  Все монеты (flat_map): {:?}", all_coins);

    // 💡 ADVICE: flat_map vs map:
    // map:   Vec<Vec<T>> -> Vec<Iterator<T>>  (итератор итераторов)
    // flat_map: Vec<Vec<T>> -> Vec<T>         (один плоский список)

    // Реальный кейс: все транзакции всех кошельков
    let wallets = vec![
        vec!["tx1_send", "tx2_recv"],            // кошелёк 1
        vec!["tx3_send", "tx4_send", "tx5_recv"],// кошелёк 2
        vec![],                                   // кошелёк 3 (пустой)
    ];

    let all_txs: Vec<&str> = wallets.iter()
        .flat_map(|w| w.iter())
        .map(|&t| t)
        .collect();

    println!("  Все транзакции: {:?}", all_txs);

    // ============================================================
    // 4️⃣ rev() — переворачиваем порядок
    //
    // 💡 ADVICE: rev() меняет порядок на обратный.
    // Полезно, когда нужно показать «последние N» записей.
    // ============================================================
    println!("\n=== 4. rev() — обратный порядок ===");

    let numbers = vec![1, 2, 3, 4, 5];
    let reversed: Vec<i32> = numbers.iter()
        .rev()
        .map(|&x| x)
        .collect();

    println!("  Оригинал:  {:?}", numbers);
    println!("  Реверс:    {:?}", reversed);

    // ============================================================
    // 5️⃣ step_by() — шаг
    //
    // 💡 ADVICE: step_by() берёт каждый N-й элемент.
    // Полезно для децимации данных или выборки.
    // ============================================================
    println!("\n=== 5. step_by() — каждый N-й элемент ===");

    let every_second: Vec<i32> = numbers.iter()
        .step_by(2)
        .map(|&x| x)
        .collect();

    println!("  Каждый 2-й: {:?}", every_second);

    // ============================================================
    // 🧠 ИТОГ:
    // chain()     — склеить два итератора в один
    // zip()       — спарить два итератора в кортежи
    // flat_map()  — развернуть вложенный итератор
    // rev()       — перевернуть порядок
    // step_by()   — взять каждый N-й
    // ============================================================
    println!("\n✅ Итог: chain, zip, flat_map — комбинируем итераторы");
}
